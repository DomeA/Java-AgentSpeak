package org.lightjason.agentspeak.action.buildin.collection.list;

import com.google.common.collect.Lists;
import org.lightjason.agentspeak.action.buildin.IBuildinAction;
import org.lightjason.agentspeak.language.CCommon;
import org.lightjason.agentspeak.language.ITerm;
import org.lightjason.agentspeak.language.execution.IContext;
import org.lightjason.agentspeak.language.fuzzy.CFuzzyValue;
import org.lightjason.agentspeak.language.fuzzy.IFuzzyValue;

import java.util.List;
import java.util.stream.Collectors;


/**
 * reverses a list.
 * All arguments of the action are lists and the action will remove
 * nested structures and reverse all elements in a single list,
 * the action fails never
 *
 * @code R = collection/list/reverse( L, [1,2], [3,4,[7,8]] ); @endcode
 */
public final class CReverse extends IBuildinAction
{
    /**
     * ctor
     */
    public CReverse()
    {
        super( 3 );
    }

    @Override
    public final int minimalArgumentNumber()
    {
        return 1;
    }

    @Override
    public final IFuzzyValue<Boolean> execute( final IContext p_context, final boolean p_parallel, final List<ITerm> p_argument, final List<ITerm> p_return
    )
    {
        // all arguments are list references
        p_return.addAll( Lists.reverse( CCommon.flatcollection( p_argument ).collect( Collectors.toList() ) ) );
        return CFuzzyValue.from( true );
    }

}
